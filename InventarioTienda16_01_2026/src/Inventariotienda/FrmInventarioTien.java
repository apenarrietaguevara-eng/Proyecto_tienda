    private void txtCantidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadKeyTyped
        char c = evt.getKeyChar();
    if (!Character.isDigit(c)) {
        evt.consume();
    }
    }//GEN-LAST:event_txtCantidadKeyTyped

    private void txtIdProductoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIdProductoKeyTyped
       char c = evt.getKeyChar();
  
    if (!Character.isDigit(c)) {
        evt.consume(); 
    }
    if (txtIdProducto.getText().length() >= 10) {
        evt.consume();
    }
    
    }//GEN-LAST:event_txtIdProductoKeyTyped

    private void txtPrecioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrecioKeyTyped
        char c = evt.getKeyChar();
    
    if (!Character.isDigit(c) && c != '.') {
        evt.consume();
    }
  
    if (c == '.' && txtPrecio.getText().contains(".")) {
        evt.consume();
    }
    
    }//GEN-LAST:event_txtPrecioKeyTyped

    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
    char c = evt.getKeyChar();
    
    if (!Character.isLetter(c) && c != java.awt.event.KeyEvent.VK_SPACE && c != java.awt.event.KeyEvent.VK_BACK_SPACE) {
        evt.consume(); 
    }
    
    }//GEN-LAST:event_txtNombreKeyTyped

