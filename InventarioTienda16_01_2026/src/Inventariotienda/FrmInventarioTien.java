    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
    String nombre , fecha , tipo ;
       double precio;
       int cantidad ;
       
       nombre = this.txtNombre.getText().trim();
       if(nombre.isEmpty()){
           JOptionPane.showMessageDialog(null,"El nombre del producto es obligatorio","validacion de datos" , JOptionPane.ERROR_MESSAGE);
           this.txtNombre.requestFocus();
           return;
       }
       
       try{
           precio = Double.parseDouble(this.txtPrecio.getText().trim());
           if(precio<0){
               JOptionPane.showMessageDialog(null , "Precio deber ser mayor a cero" , " Validacion de Datos" , JOptionPane.ERROR_MESSAGE);
               this.txtPrecio.requestFocus();
               return;
           }
       }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(null , "Precio debe ser un numero " ,"Validacion de datos" , JOptionPane.ERROR_MESSAGE);
           this.txtPrecio.requestFocus();
           return;
           
       }
       try{
            cantidad = Integer.parseInt(this.txtCantidad.getText().trim());
            if(cantidad<0){
                JOptionPane.showMessageDialog(null,"Cantidad debe ser mayor a cero" ,"Validacion de datos" , JOptionPane.ERROR_MESSAGE);
                this.txtCantidad.requestFocus();
                return;
            } 
       }catch(NumberFormatException e ){
            JOptionPane.showMessageDialog(null ,"Cantidad debe ser un numero" , "Validacion de datos" , JOptionPane.ERROR_MESSAGE);
            this.txtCantidad.requestFocus();
            return;
            
       }
       String tipos = this.cmbTipos.getSelectedItem().toString();
       if (tipos.equals("Comestible")) {
    if (this.txtFecha.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Fecha Obligatoria", "Validacion", 0);
        return; 
    }
     }

       fecha = this.txtFecha.getText().trim();
       if(tipos.equals("Comestible")){
           if(fecha.isEmpty()){
               JOptionPane.showMessageDialog(null,"Fecha Obligatortio" , "Validacion de datos", JOptionPane.ERROR_MESSAGE);
               this.txtFecha.requestFocus();
               return;
           }
       }
       if (tipos.equals("Comestible")) {
        if (fecha.length() != 10) {
            JOptionPane.showMessageDialog(this, "La fecha debe ser completa (dd/mm/aaaa).", "Error Fecha", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            int dia = Integer.parseInt(fecha.substring(0, 2));
            int mes = Integer.parseInt(fecha.substring(3, 5));
            int anio = Integer.parseInt(fecha.substring(6));
            if (dia < 1 || dia > 31 || mes < 1 || mes > 12 || anio < 2024) {
                JOptionPane.showMessageDialog(this, "La fecha ingresada no es lógica.", "Error Fecha", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La fecha contiene caracteres inválidos.", "Error Fecha", JOptionPane.ERROR_MESSAGE);
            return;
        }
       
       if(tipos.equals("Limpieza")){
           if(!fecha.isEmpty()){
               JOptionPane.showMessageDialog(null,"Producto limpieza no requiere fecha" , "Validacion de datos" , JOptionPane.INFORMATION_MESSAGE);
               this.txtFecha.setText("");
               return;
               
           }
       }
       String idProducto = this.txtIdProducto.getText().trim();
       if(idProducto.isEmpty()){
           JOptionPane.showMessageDialog(null ," ID obligatorio" , "validacion de datos" ,JOptionPane.ERROR_MESSAGE);
           this.txtIdProducto.requestFocus();
           return;
       }
       
       JOptionPane.showMessageDialog(this, "¡El producto ha sido guardado exitosamente!", "Registro Exitoso", JOptionPane.INFORMATION_MESSAGE);
        this.txtNombre.setText("");        
        this.txtPrecio.setText("");        
        this.txtCantidad.setText("");      
        this.txtIdProducto.setText("");    
        this.txtFecha.setText("");         
        this.cmbTipos.setSelectedIndex(0); 
    
    this.txtNombre.requestFocus();  
      } 
    }//GEN-LAST:event_btnGrabarActionPerformed

